public class WellFormed {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.println("A well-formed Java program has \na main method with { and } \nbraces.");
        System.out.println();
        System.out.println("A System.out.println statement \nhas ( and ) usually a \nString that starts and ends with a \" character.");
        System.out.println("has ( and ) usually a");
        System.out.println("String that starts and ends with a");
        System.out.println("(But we type \\\" instead!)");
    }
    
}
