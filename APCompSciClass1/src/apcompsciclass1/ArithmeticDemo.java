public class ArithmeticDemo {
    public static void main(String[] args){
        //Addition
        System.out.println(2+3);
        //Subtraction
        System.out.println(2-3);
        //Multiplication
        System.out.println(4*2);
        //division
        System.out.println(4/2);
        System.out.println(10/3);//would give 3
        System.out.println(20/3);//would give 6
        //Integer division: when and integer is divided by a integer
        //java truncates (gets rid of) the decimal portion and only leaves
        //the integer Quptient
        //Mod
        System.out.println(10%3);//1
        System.out.println(10%5);//0
        //gives you the remainder
    }
}
